public class Coding_Relational_Operatoren {

    public static void main(String[] args) {
        int a = 52;
        int b = 2;

        System.out.println(a + b);

        System.out.println(a == b);

        System.out.println(a > b);

        System.out.println(a < b);

        double c = 5.2;  //Float udn double sind vom output gleich (Float 32 bit double 64 bit)
        double d = 0.2;

        System.out.println(c + d);

        System.out.println(c == d);

        System.out.println(c > d);

        System.out.println(c < d);


    }
}
